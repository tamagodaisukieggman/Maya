def create_controls(types_names_dict=None):
    return [create_control(ctrl_type, name) for ctrl_type, name in types_names_dict.items()]

def create_control(ctrl_type=None, name=None, rgb=None):
    curve_settings = {}
    curve_settings['d'] = CONTROLLER_SETTINGS[ctrl_type]['degree']
    curve_settings['p'] = CONTROLLER_SETTINGS[ctrl_type]['points']
    curve_settings['k'] = CONTROLLER_SETTINGS[ctrl_type]['knot']
    crv = cmds.curve(**curve_settings)
    set_rgb_color(crv, rgb)
    if CONTROLLER_SETTINGS[ctrl_type]['periodic']:
        cmds.closeCurve(crv, ch=True, ps=0, rpo=True, bb=0.5, bki=False, p=0.1)
    if name:
        cmds.rename(crv, name)
        cmds.select(name, r=True)
        return name
    else:
        cmds.select(crv, r=True)
        return crv

def set_rgb_color(ctrl, color=(1,1,1)):
    rgb = ("R","G","B")
    shape = cmds.listRelatives(ctrl, s=1)[0]
    cmds.setAttr(shape + ".overrideEnabled",1)
    cmds.setAttr(shape + ".overrideRGBColors",1)
    for channel, color in zip(rgb, color):
        cmds.setAttr(shape + ".overrideColor{}".format(channel), color)

def copy_curve_settings_to_clipboard():
    sel = cmds.ls(os=True)
    if not sel:
        return

    get_points = {}
    for obj in sel:
        cvs = cmds.ls(obj+'.cv[*]', fl=True)
        points = [cmds.pointPosition(cv, w=True) for cv in cvs]
        shape = cmds.listRelatives(obj, s=True) or list()
        degree = 1
        periodic = False
        if shape:
            degree = cmds.getAttr(shape[0]+'.degree')
            form = cmds.getAttr(shape[0]+'.form')
            if form == 2:
                periodic = True

        get_points[obj] = {}
        get_points[obj]['points'] = points
        get_points[obj]['degree'] = degree
        get_points[obj]['knot'] = create_knots(obj)
        get_points[obj]['periodic'] = periodic


    str_get_points = pprint.pformat(get_points, width=150)
    # print('get_points = {}'.format(str_get_points))
    function_text = '''
controllers = []
for n, points in get_points.items():
    curve_settings = {}
    curve_settings['d'] = points['degree']
    curve_settings['p'] = points['points']
    curve_settings['k'] = points['knot']
    crv = cmds.curve(**curve_settings)
    if points['periodic']:
        cmds.closeCurve(crv, ch=True, ps=0, rpo=True, bb=0.5, bki=False, p=0.1)
    controllers.append(crv)
    '''

    text = 'get_points = ' + str_get_points + '\n' + function_text

    copy_to_clipboard(text)

def merge_curves():
    sel = cmds.ls( os=True )
    # cmds.select(sel[0], r=True)
    # cmds.makeIdentity(apply=True, t=True, r=True, s=True, n=False, pn=True)
    if not sel:
        return
    shape = cmds.listRelatives(sel[0], s=True, f=True) or list()
    # cmds.select(sel[0], r=True)
    # mel.eval('channelBoxCommand -freezeAll;')
    if shape:
        for sh in shape:
            cmds.parent(sh, sel[1], s=True, r=True)

    cmds.delete(sel[0])

    cmds.select(sel[1], r=True)

# Copy to Clipboard
def copy_to_clipboard(text):
    cb = QtWidgets.QApplication.clipboard()
    cb.setText(text)

# Curve Knots
def create_knots(crv):
    knots = mel.eval('string $cInfo = `arclen -ch 1 {}`;getAttr($cInfo+".knots[*]");'.format(crv))
    mel.eval('delete $cInfo;')
    return knots

class ControllerLibrary():
    def __init__(self):
        self.CONTROLLER_SETTINGS = CONTROLLER_SETTINGS
        self.MAIN_WINDOW = 'ControllerLibrary'
        self.history_items = {}
        self.optionVar_key = self.MAIN_WINDOW + '_optionVar_key'
        self.ctrl_color = [0.0287, 0.0158, 0.1857]

    def show(self):
        if cmds.workspaceControl(self.MAIN_WINDOW, q=True, ex=True):
            cmds.deleteUI(self.MAIN_WINDOW)

        self.win = cmds.workspaceControl(self.MAIN_WINDOW, l=self.MAIN_WINDOW)

        self.layout()

        cmds.showWindow(self.win)

        self.load_settings()

    def layout(self):
        menuBarLayout = cmds.menuBarLayout(p=self.win)
        self.create_menu = cmds.menu(label='Create', to=True)
        for ctrl_base_type, ctrl_types in CTRL_TYPES.items():
            ctrl_base_item = cmds.menuItem(l=ctrl_base_type, p=self.create_menu, sm=True, to=True)
            for ctrl_type in ctrl_types:
                cmds.menuItem(l=ctrl_type, c=partial(self.create_control_ui, ctrl_type=ctrl_type), p=ctrl_base_item, to=True)
            cmds.setParent('..', menu=True)

        self.history_menu = cmds.menu(label='History', to=True)

        self.edit_menu = cmds.menu(label='Edit')
        cmds.menuItem(l='Reset Settings', c=partial(self.reset_settings), p=self.edit_menu, to=False)
        cmds.menuItem(l='Reset Color Settings', c=partial(self.reset_color_settings), p=self.edit_menu, to=False)

        self.main_row_col_layout = cmds.rowColumnLayout(numberOfColumns=3, columnWidth=[(1, 60)])
        self.color_btn = cmds.button(l='Color', c=partial(self.set_ctrl_color), bgc=self.ctrl_color, p=self.main_row_col_layout)
        self.merge_crvs_btn = cmds.button(l='Merge', c=partial(self.merge_curves_ui), p=self.main_row_col_layout)
        self.copy_crv_to_clipboard_btn = cmds.button(l='Copy Curve Settings to Clipboard', c=partial(self.copy_curve_settings_to_clipboard_ui), p=self.main_row_col_layout)
        self.set_selected_ctrl_color_btn = cmds.button(l='Set Color', c=partial(self.set_selected_ctrl_color), p=self.main_row_col_layout)


    def create_control_ui(self, dummy, ctrl_type=None):
        create_control(ctrl_type=ctrl_type, name=None, rgb=self.ctrl_color)
        if not ctrl_type in self.history_items.keys():
            self.add_func_menuItem(name=ctrl_type)

        self.save_settings()

    def copy_curve_settings_to_clipboard_ui(self, dummy):
        copy_curve_settings_to_clipboard()

    def merge_curves_ui(self, dummy):
        merge_curves()

    def add_func_menuItem(self, name=None):
        ctrl_menuitem = cmds.menuItem(l=name, c=partial(self.create_control_ui, ctrl_type=name), p=self.history_menu)
        self.history_items[name] = ctrl_menuitem

    def set_ctrl_color(self, dummy):
        res = cmds.colorEditor()
        self.ctrl_color = cmds.colorEditor(query=True, rgb=True)
        cmds.button(self.color_btn, e=True, bgc=self.ctrl_color)

    def set_selected_ctrl_color(self, dummy):
        sel = cmds.ls(os=True)
        if not sel:
            return
        [set_rgb_color(ctrl, color=self.ctrl_color) for ctrl in sel]

    def save_settings(self):
        save_items = {}
        save_items[self.optionVar_key] = [ctrl_type for ctrl_type in self.history_items.keys()]

        for key, value in save_items.items():
            v = str(value)
            cmds.optionVar(sv=[key, v])

    def load_settings(self):
        self.load_values = load_optionvar(self.optionVar_key)
        if self.load_values:
            for ctrl_type in self.load_values:
                self.add_func_menuItem(name=ctrl_type)

    def reset_settings(self, dummy):
        cmds.optionVar(rm=self.optionVar_key)
        print('Deleted {} Settigs.'.format(self.MAIN_WINDOW))

    def reset_color_settings(self, dummy):
        self.ctrl_color = [0.0287, 0.0158, 0.1857]
        cmds.button(self.color_btn, e=True, bgc=self.ctrl_color)
        print('Set Default Color Settigs.')

def load_optionvar(key):
    if cmds.optionVar(ex=key):
        return eval(cmds.optionVar(q=key))
    else:
        return None


