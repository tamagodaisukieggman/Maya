def getJointsExceptADN():
    null = cmds.ls('null')
    if not null:
        return

    joints = cmds.ls(null, dag=True, type='joint')
    result_list = []
    for joint in joints:
        if not joint.startswith('_a'):
            result_list.append(joint)
    return result_list


