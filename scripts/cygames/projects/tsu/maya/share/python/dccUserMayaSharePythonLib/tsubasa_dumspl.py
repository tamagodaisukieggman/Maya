def getId(scene_path):
    """シーン名からid取得"""    
    scene_name = os.path.basename(scene_path)
    if 'wpbase' in scene_name:
         result = re.match('\w\w\w\w\w\w[0-9A-Fa-f]{4}', scene_name)
    else:
         result = re.match('\w\w[0-9A-Fa-f]{4}', scene_name)
        
    if result is None:
        return None
    else:
        return result.group()


def _getIdFromRoot():
    """シーン内のノードからid取得（mayaのみ）"""

    id_ = None
    top_nodes = cmds.ls(assemblies=True)
    for node in top_nodes:
        if re.match('\w\w[0-9A-Fa-f]{4}', node):
            id_ = node
            break
    return id_


